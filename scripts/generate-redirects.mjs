#!/usr/bin/env node
import { writeFileSync } from 'node:fs';
import blogSlugs from '../src/data/blog/slugs.json' with { type: 'json' };

const legacyLocationSlugs = [
	'private-investigator-toronto-ontario',
	'private-investigator-brampton-ontario',
	'private-investigator-vaughan-ontario',
	'private-investigator-markham-ontario',
	'private-investigator-mississauga-ontario',
	'private-investigator-richmond-hill-ontario',
];

const locationAnchors = {
	'private-investigator-toronto-ontario': 'toronto',
	'private-investigator-brampton-ontario': 'brampton',
	'private-investigator-vaughan-ontario': 'vaughan',
	'private-investigator-markham-ontario': 'markham',
	'private-investigator-mississauga-ontario': 'mississauga',
	'private-investigator-richmond-hill-ontario': 'richmond-hill',
};

const lines = [
	'# Auto-generated by scripts/generate-redirects.mjs',
	'# Legacy WordPress blog URLs (root slug)',
	...blogSlugs.map((slug) => `/${slug} /blog/${slug} 301`),
	'',
	'# Legacy WordPress blog URLs (root slug with trailing slash)',
	...blogSlugs.map((slug) => `/${slug}/ /blog/${slug} 301`),
	'',
	'# Legacy location pages to unified contact page',
	...legacyLocationSlugs.map(
		(slug) => `/${slug} /contact#${locationAnchors[slug]} 301`,
	),
	...legacyLocationSlugs.map(
		(slug) => `/${slug}/ /contact#${locationAnchors[slug]} 301`,
	),
	'',
	'# Blog post URLs with trailing slash',
	'/blog/:slug/ /blog/:slug 301',
	'',
	'# Strip trailing slashes on all other routes (prerendered pages on Netlify)',
	'/*/ /:splat 301',
	'',
];

writeFileSync(new URL('../public/_redirects', import.meta.url), lines.join('\n'));
console.log(`Wrote ${lines.filter((l) => l && !l.startsWith('#')).length} redirect rules to public/_redirects`);
